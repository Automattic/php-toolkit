generate_pmd_toolkit_autoload() {
	local target_dir=$1

	mkdir -p "$target_dir/vendor/composer"
	cp "$PROJECT_DIR/vendor/composer/ClassLoader.php" "$target_dir/vendor/composer/"

	PMD_PROJECT_DIR="$PROJECT_DIR" PMD_TOOLKIT_TARGET="$target_dir" php <<'PHP'
<?php

$project_dir = rtrim( realpath( getenv( 'PMD_PROJECT_DIR' ) ), '/' );
$target_dir  = rtrim( getenv( 'PMD_TOOLKIT_TARGET' ), '/' );

function pmd_toolkit_relative_path( $project_dir, $path ) {
	if ( 0 !== strpos( $path, $project_dir . '/' ) ) {
		return null;
	}

	return substr( $path, strlen( $project_dir ) + 1 );
}

function pmd_toolkit_file_exists( $target_dir, $relative_path ) {
	return is_file( $target_dir . '/' . $relative_path );
}

function pmd_toolkit_dir_exists( $target_dir, $relative_path ) {
	return is_dir( $target_dir . '/' . $relative_path );
}

function pmd_toolkit_php_header() {
	return "<?php\n\n// autoload metadata generated by bin/build-plugins.sh for Push MD.\n\n" .
		"\$vendorDir = dirname(__DIR__);\n" .
		"\$baseDir = dirname(\$vendorDir);\n\n";
}

function pmd_toolkit_export_path_expression( $relative_path ) {
	return '$baseDir . ' . var_export( '/' . $relative_path, true );
}

function pmd_toolkit_write_classmap( $target_dir, $classmap ) {
	ksort( $classmap );
	$code = pmd_toolkit_php_header() . "return array(\n";
	foreach ( $classmap as $class => $relative_path ) {
		$code .= '    ' . var_export( $class, true ) . ' => ' . pmd_toolkit_export_path_expression( $relative_path ) . ",\n";
	}
	$code .= ");\n";

	file_put_contents( $target_dir . '/vendor/composer/autoload_classmap.php', $code );
}

function pmd_toolkit_write_psr4( $target_dir, $psr4 ) {
	ksort( $psr4 );
	$code = pmd_toolkit_php_header() . "return array(\n";
	foreach ( $psr4 as $prefix => $paths ) {
		$paths = array_values( array_unique( $paths ) );
		sort( $paths );
		$code .= '    ' . var_export( $prefix, true ) . " => array(\n";
		foreach ( $paths as $relative_path ) {
			$code .= '        ' . pmd_toolkit_export_path_expression( $relative_path ) . ",\n";
		}
		$code .= "    ),\n";
	}
	$code .= ");\n";

	file_put_contents( $target_dir . '/vendor/composer/autoload_psr4.php', $code );
}

function pmd_toolkit_write_files( $target_dir, $files ) {
	ksort( $files );
	$code = pmd_toolkit_php_header() . "return array(\n";
	foreach ( $files as $identifier => $relative_path ) {
		$code .= '    ' . var_export( $identifier, true ) . ' => ' . pmd_toolkit_export_path_expression( $relative_path ) . ",\n";
	}
	$code .= ");\n";

	file_put_contents( $target_dir . '/vendor/composer/autoload_files.php', $code );
}

$classmap = array();
$classmap_excludes = array(
	'components/Markdown/class-markdownimporter.php' => true,
);
foreach (
	array(
		$project_dir . '/vendor/composer/autoload_classmap.php',
		$project_dir . '/components/Markdown/vendor-patched/composer/autoload_classmap.php',
	) as $source_map
) {
	$source_classmap = require $source_map;
	foreach ( $source_classmap as $class => $source_file ) {
		$relative_path = pmd_toolkit_relative_path( $project_dir, $source_file );
		if ( null === $relative_path || ! pmd_toolkit_file_exists( $target_dir, $relative_path ) ) {
			continue;
		}
		if ( isset( $classmap_excludes[ $relative_path ] ) ) {
			continue;
		}
		if ( 0 === strpos( $relative_path, 'components/Markdown/vendor-patched/composer/' ) ) {
			continue;
		}
		$classmap[ $class ] = $relative_path;
	}
}

$psr4 = array();
foreach (
	array(
		$project_dir . '/vendor/composer/autoload_psr4.php',
		$project_dir . '/components/Markdown/vendor-patched/composer/autoload_psr4.php',
	) as $source_map
) {
	$source_psr4 = require $source_map;
	foreach ( $source_psr4 as $prefix => $source_paths ) {
		foreach ( $source_paths as $source_path ) {
			$relative_path = pmd_toolkit_relative_path( $project_dir, $source_path );
			if ( null === $relative_path || ! pmd_toolkit_dir_exists( $target_dir, $relative_path ) ) {
				continue;
			}
			if ( ! isset( $psr4[ $prefix ] ) ) {
				$psr4[ $prefix ] = array();
			}
			$psr4[ $prefix ][] = $relative_path;
		}
	}
}

$files = array();
foreach (
	array(
		$project_dir . '/vendor/composer/autoload_files.php',
		$project_dir . '/components/Markdown/vendor-patched/composer/autoload_files.php',
	) as $source_map
) {
	$source_files = require $source_map;
	foreach ( $source_files as $identifier => $source_file ) {
		$relative_path = pmd_toolkit_relative_path( $project_dir, $source_file );
		if ( null === $relative_path || ! pmd_toolkit_file_exists( $target_dir, $relative_path ) ) {
			continue;
		}
		$files[ $identifier ] = $relative_path;
	}
}

pmd_toolkit_write_classmap( $target_dir, $classmap );
pmd_toolkit_write_psr4( $target_dir, $psr4 );
file_put_contents( $target_dir . '/vendor/composer/autoload_namespaces.php', pmd_toolkit_php_header() . "return array();\n" );
pmd_toolkit_write_files( $target_dir, $files );
PHP
}

harden_pmd_bundled_php() {
	local target_dir=$1

	PMD_BUNDLE_TARGET="$target_dir" php <<'PHP'
<?php

$target_dir = rtrim( getenv( 'PMD_BUNDLE_TARGET' ), '/' );

$phpcs_disable = '// phpcs:disable WordPress.NamingConventions.PrefixAllGlobals.NonPrefixedNamespaceFound,WordPress.NamingConventions.PrefixAllGlobals.NonPrefixedFunctionFound,WordPress.Security.EscapeOutput.ExceptionNotEscaped,WordPress.Security.EscapeOutput.OutputNotEscaped,WordPress.PHP.DevelopmentFunctions.error_log_trigger_error,WordPress.PHP.DevelopmentFunctions.error_log_debug_backtrace,WordPress.PHP.DevelopmentFunctions.error_log_print_r,WordPress.PHP.DevelopmentFunctions.error_log_var_export,WordPress.PHP.DevelopmentFunctions.error_log_set_error_handler,WordPress.WP.AlternativeFunctions,PluginCheck.CodeAnalysis.Heredoc.NotAllowed' . "\n";
$guard         = "if ( ! defined( 'ABSPATH' ) ) {\n\texit;\n}\n\n";

function push_md_add_phpcs_disable( $code, $phpcs_disable ) {
	if ( false !== strpos( $code, 'phpcs:disable WordPress.NamingConventions.PrefixAllGlobals.NonPrefixedNamespaceFound' ) ) {
		return $code;
	}

	return preg_replace( '/\A<\?php\s*/', "<?php\n" . $phpcs_disable . "\n", $code, 1 );
}

function push_md_add_direct_access_guard( $code, $guard ) {
	if (
		false !== strpos( $code, "defined( 'ABSPATH' )" ) ||
		false !== strpos( $code, "defined( 'WP_UNINSTALL_PLUGIN' )" )
	) {
		return $code;
	}

	if ( ! preg_match( '/\A<\?php\b/', $code ) ) {
		return $code;
	}

	if ( preg_match( '/namespace\s+[^;]+;\s*/', $code, $matches, PREG_OFFSET_CAPTURE ) ) {
		$insert_at = $matches[0][1] + strlen( $matches[0][0] );
		$tail      = substr( $code, $insert_at );
		if ( preg_match( '/\A(?:(?:\s+)|(?:use\s+[^;]+;\s*))*/', $tail, $use_matches ) ) {
			$insert_at += strlen( $use_matches[0] );
		}

		return substr( $code, 0, $insert_at ) . $guard . substr( $code, $insert_at );
	}

	if ( preg_match( '/\A<\?php\s*(?:declare\s*\([^;]+;\s*)*/', $code, $matches ) ) {
		$insert_at = strlen( $matches[0] );

		return substr( $code, 0, $insert_at ) . $guard . substr( $code, $insert_at );
	}

	return $code;
}

$iterator = new RecursiveIteratorIterator(
	new RecursiveDirectoryIterator(
		$target_dir,
		FilesystemIterator::SKIP_DOTS
	)
);

foreach ( $iterator as $file ) {
	if ( 'php' !== strtolower( $file->getExtension() ) ) {
		continue;
	}

	$path          = $file->getPathname();
	$relative_path = substr( $path, strlen( $target_dir ) + 1 );
	$code          = file_get_contents( $path );
	if ( false === $code ) {
		continue;
	}

	if ( 0 === strpos( $relative_path, 'php-toolkit/' ) ) {
		$code = push_md_add_phpcs_disable( $code, $phpcs_disable );
	}

	$code = push_md_add_direct_access_guard( $code, $guard );
	file_put_contents( $path, $code );
}
PHP
}

copy_pmd_php_toolkit_bundle() {
	local target_dir=$1
	local manifest_file="$PROJECT_DIR/bin/push-md-toolkit-manifest.txt"
	local manifest_path

	mkdir -p "$target_dir"
	while IFS= read -r manifest_path || [ -n "$manifest_path" ]; do
		if [[ -z "$manifest_path" || "$manifest_path" =~ ^[[:space:]]*# ]]; then
			continue
		fi
		copy_pmd_toolkit_path "$target_dir" "$manifest_path"
	done < "$manifest_file"

	generate_pmd_toolkit_autoload "$target_dir"
}

